/**
 * Generate version.js from package.json
 * This ensures the CDN fallback URL uses the correct package version
 */
import { readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const packageJsonPath = join(__dirname, '..', 'package.json');
const versionJsPath = join(__dirname, '..', 'version.js');

const packageJson = JSON.parse(readFileSync(packageJsonPath, 'utf-8'));

const content = `// Auto-generated by scripts/generate-version.js - DO NOT EDIT
export const packageVersion = '${packageJson.version}';
`;

writeFileSync(versionJsPath, content);
console.log(`Generated version.js with packageVersion: ${packageJson.version}`);
